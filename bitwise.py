b = 3  # 0011

print(a & b)  # AND => 1
print(a | b)  # OR  => 7
print(a ^ b)  # XOR => 6
print(~a)     # NOT => -6
print(a << 1) # Left shift => 10
print(a >> 1) # Right shift => 2
