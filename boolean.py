# Boolean values and relational operators

# Relational operators return boolean values: True or False
a = 10
b = 7

print("a > b:", a > b)          # True
print("a == b:", a == b)        # False
print("a <= 10:", a <= 10)      # True
print("a != b:", a != b)        # True

# Boolean values directly
x = True
y = False

print("not x:", not x)          # False
print("x and y:", x and y)      # False
print("x or y:", x or y)        # True

